package net.spring.intranet.entidad;

import java.io.Serializable;

public class Docentes implements Serializable{
	
	private int codigoPro;
	private String nomPro;
	private String apePro;
	private String usernamePro;
	private String passPro;
	private int edadPro;
	private String celPro;
	private String direcPro;
	private int codRolPro;
	private String DesRolPro;
	private String estado;
	
	
	public int getCodigoPro() {
		return codigoPro;
	}
	public void setCodigoPro(int codigoPro) {
		this.codigoPro = codigoPro;
	}
	public String getNomPro() {
		return nomPro;
	}
	public void setNomPro(String nomPro) {
		this.nomPro = nomPro;
	}
	public String getApePro() {
		return apePro;
	}
	public void setApePro(String apePro) {
		this.apePro = apePro;
	}
	public String getUsernamePro() {
		return usernamePro;
	}
	public void setUsernamePro(String usernamePro) {
		this.usernamePro = usernamePro;
	}
	public String getPassPro() {
		return passPro;
	}
	public void setPassPro(String passPro) {
		this.passPro = passPro;
	}
	public int getEdadPro() {
		return edadPro;
	}
	public void setEdadPro(int edadPro) {
		this.edadPro = edadPro;
	}
	public String getCelPro() {
		return celPro;
	}
	public void setCelPro(String celPro) {
		this.celPro = celPro;
	}
	public String getDirecPro() {
		return direcPro;
	}
	public void setDirecPro(String direcPro) {
		this.direcPro = direcPro;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	public int getCodRolPro() {
		return codRolPro;
	}
	public void setCodRolPro(int codRolPro) {
		this.codRolPro = codRolPro;
	}
	public String getDesRolPro() {
		return DesRolPro;
	}
	public void setDesRolPro(String desRolPro) {
		DesRolPro = desRolPro;
	}
	
	
	
	
	
	

}
