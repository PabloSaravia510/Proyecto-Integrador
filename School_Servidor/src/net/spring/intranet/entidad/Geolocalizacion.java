package net.spring.intranet.entidad;

public class Geolocalizacion {
	private int codigoGeolocalizacion;
	
	private String coordenadasGeolocalizacion;
	
	private int codigoAlumno;

	public int getCodigoGeolocalizacion() {
		return codigoGeolocalizacion;
	}

	public void setCodigoGeolocalizacion(int codigoGeolocalizacion) {
		this.codigoGeolocalizacion = codigoGeolocalizacion;
	}

	public String getCoordenadasGeolocalizacion() {
		return coordenadasGeolocalizacion;
	}

	public void setCoordenadasGeolocalizacion(String coordenadasGeolocalizacion) {
		this.coordenadasGeolocalizacion = coordenadasGeolocalizacion;
	}

	public int getCodigoAlumno() {
		return codigoAlumno;
	}

	public void setCodigoAlumno(int codigoAlumno) {
		this.codigoAlumno = codigoAlumno;
	}
	
	
}
